void display(char word[])
{
	int count = 0;
	while(word[count]!='\0'){
		count++;
	}
	bool billboardLetters[count][25];
	for(int i=0; i<count;i++){
		for(int j=0; j<25;j++){
			if (word[i]==' ')
			{
				billboardLetters[i][j] = ignore[26][j];
				continue;
			}
			if (word[i] == '!')
			{
				billboardLetters[i][j] = ignore[27][j];
				continue;
			}
			billboardLetters[i][j] = ignore[(int)word[i] - 65][j];
		}
	}
	for (int i = 0; i < 5; i++)
	{
		for(int j=0, m=0; ;j++){
			if (billboardLetters[m][i*5+j])
			{
				printf("#");
			}
			else
			{
				printf(" ");
			}
			if(j%4==0 && j!=0){
				m++;
				printf(" ");
				if(m *j >= 4 * count){
					break;
				}
				j = -1;
			}
		}
		printf("\n");
		
	}
}

